// Input validation schema
const messageSchema = {
  isValidRole: (role: unknown): role is 'user' | 'assistant' => 
    role === 'user' || role === 'assistant',
  isValidContent: (content: unknown): content is string => 
    typeof content === 'string' && content.length > 0 && content.length <= 10000,
  isValidMessage: (msg: unknown): msg is { role: 'user' | 'assistant'; content: string } =>
    typeof msg === 'object' && msg !== null &&
    'role' in msg && 'content' in msg &&
    messageSchema.isValidRole((msg as any).role) &&
    messageSchema.isValidContent((msg as any).content),
  isValidMessages: (msgs: unknown): msgs is Array<{ role: 'user' | 'assistant'; content: string }> =>
    Array.isArray(msgs) && msgs.length > 0 && msgs.length <= 50 && msgs.every(messageSchema.isValidMessage),
  isValidLanguage: (lang: unknown): lang is 'ar' | 'en' =>
    lang === undefined || lang === 'ar' || lang === 'en'
};

